// src/models/storefront/storefrontPage.model.js
const mongoose = require('mongoose');

// Sub-schema for individual sections (Hero, Product Grid, etc.)
const sectionSchema = new mongoose.Schema({
  id: { type: String, required: true }, 
  type: { type: String, required: true }, // e.g. 'hero_banner'
  
  position: { type: Number, required: true },
  
  // The content configuration
  config: { type: mongoose.Schema.Types.Mixed, default: {} },
  
  // Data Source logic
  dataSource: { 
    type: String, 
    enum: ['static', 'smart', 'manual', 'dynamic', 'category'], 
    default: 'static' 
  },
  
  // Link to dynamic data
  smartRuleId: { type: mongoose.Schema.Types.ObjectId, ref: 'SmartRule' },
  manualData: { 
    productIds: [{ type: mongoose.Schema.Types.ObjectId, ref: 'Product' }],
    categoryIds: [{ type: mongoose.Schema.Types.ObjectId, ref: 'Master' }]
  },

  isActive: { type: Boolean, default: true }
}, { _id: false });

const pageSchema = new mongoose.Schema({
  organizationId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Organization',
    required: true,
    index: true
  },

  name: { type: String, required: true, trim: true, maxlength: 100 },
  slug: { 
    type: String, 
    required: true, 
    lowercase: true,
    trim: true,
    match: [/^[a-z0-9-/]+$/, 'Invalid slug format']
  },

  pageType: {
    type: String,
    enum: ['home', 'product', 'category', 'blog', 'content', 'policy', 'system'],
    default: 'content'
  },

  sections: [sectionSchema],

  // SEO settings
  seo: {
    title: String,
    description: String,
    keywords: [String],
    ogImage: String,
    noIndex: { type: Boolean, default: false }
  },

  // Theme Override: A specific page can look different (e.g., 'theme-dark' for Black Friday)
  pageThemeId: { type: String }, 

  status: { type: String, enum: ['draft', 'published', 'archived'], default: 'draft', index: true },
  isPublished: { type: Boolean, default: false },
  publishedAt: { type: Date },

  // System Flags
  isHomepage: { type: Boolean, default: false },
  isSystemPage: { type: Boolean, default: false }, // If true, cannot be deleted
  
  // Soft Delete for safety
  isDeleted: { type: Boolean, default: false, index: true },

  viewCount: { type: Number, default: 0 }

}, { timestamps: true });

pageSchema.index({ organizationId: 1, slug: 1 }, { unique: true });
pageSchema.index({ organizationId: 1, isHomepage: 1 });

module.exports = mongoose.model('StorefrontPage', pageSchema);


